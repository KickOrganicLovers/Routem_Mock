import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient()

async function main() {
    const users =  [
        {name: 'lychee'},
        {name: 'avocado'}
    ]

    for(const user of users) {
        await prisma.user.create({
            data: {
                name: user.name,
            }
        })
    }
}

//実行処理
main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
